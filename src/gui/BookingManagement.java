    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String s_location = jTextField6.getText();
        String e_location = jTextField7.getText();
        String s_time = jTextField3.getText();
        String e_time = jTextField5.getText();
        String passenger = jComboBox1.getSelectedItem().toString();
        String car = jComboBox2.getSelectedItem().toString();
        String car_type = jComboBox2.getSelectedItem().toString();
        Date s_date = jDateChooser1.getDate();
        Date e_date = jDateChooser2.getDate();

        if (passenger.equals("--Select--")) {
            JOptionPane.showMessageDialog(this, "Please select passenger", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (car_type.equals("--Select--")) {
            JOptionPane.showMessageDialog(this, "Please select car type", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (car.equals("--Select--")) {
            JOptionPane.showMessageDialog(this, "Please select car", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (s_location.isBlank()) {
            JOptionPane.showMessageDialog(this, "Please enter start location", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (e_location.isBlank()) {
            JOptionPane.showMessageDialog(this, "Please enter end location", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (s_date == null) {
            JOptionPane.showMessageDialog(this, "Invalid start date", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (e_date == null) {
            JOptionPane.showMessageDialog(this, "Invalid end date", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                String p[] = passenger.split("\\s+");
                ResultSet rs1 = DBConnection.search("SELECT `id` FROM `passenger` WHERE `fname` = '" + p[0] + "'");
                rs1.next();

                ResultSet rs3 = DBConnection.search("SELECT `id` FROM `car` WHERE `number` = '" + car + "'");
                rs3.next();

                SimpleDateFormat sdf1 = new SimpleDateFormat("yyyy-MM-dd");

                DBConnection.iud("INSERT INTO `booking`(`sdate`,`stime`,`return_date`,`return_time`,`start_location`,`end_location`,`passenger_id`,`car_id`) VALUES('" + sdf1.format(s_date) + "','" + s_time + "','" + sdf1.format(e_date) + "','" + e_time + "','" + s_location + "','" + e_location + "','" + rs1.getString("id") + "','" + rs3.getString("id") + "')");
                jTextField3.setText("");
                jTextField5.setText("");
                jTextField6.setText("");
                jTextField7.setText("");
                jComboBox1.setSelectedIndex(0);
                jComboBox2.setSelectedIndex(0);
                jComboBox3.setSelectedIndex(0);
                jDateChooser1.setDate(null);
                jDateChooser2.setDate(null);
                jComboBox1.grabFocus();
                loadData();
            } catch (Exception e) {
                e.printStackTrace();
            }

        }
    }//GEN-LAST:event_jButton1ActionPerformed

