    public void loadData() {
        try {
            ResultSet rs = DBConnection.search("SELECT * FROM `booking` INNER JOIN `passenger` ON `booking`.`passenger_id` = `passenger`.`id` INNER JOIN `car` ON `booking`.`car_id` = `car`.`id` INNER JOIN `booking_status` ON `booking`.`booking_status_id` = `booking_status`.`id`");
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("booking.id"));
                v.add(rs.getString("passenger.fname") + " " + rs.getString("passenger.lname"));
                v.add(rs.getString("car.number"));
                v.add(rs.getString("booking.sdate"));
                v.add(rs.getString("booking.stime"));
                v.add(rs.getString("booking.return_date"));
                v.add(rs.getString("booking.return_time"));
                v.add(rs.getString("booking.start_location"));
                v.add(rs.getString("booking.end_location"));
                v.add(rs.getString("booking_status.name"));

                dtm.addRow(v);
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

